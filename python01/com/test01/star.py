'''
문제1.
*
**
***
****
*****
'''
for i in range(5): # 0~4
    print('*' * (i + 1))

print('------')

'''
문제2.
*****   '*' * 5
****    '*' * 4
***
**
*       '*' * 1
'''
for i in range(5): # 0~4
    print('*' * (5-i))


'''
문제3.
    *     ' ' * 4 + '*' * 1
   **     ' ' * 3 + '*' * 2 
  ***
 ****
*****     ' ' * 0 + '*' * 5
'''
for i in range(5):
    print(' ' * (4 - i) + '*' * (i + 1))

'''
문제4.
*****    ' ' * 0 + '*' * 5
 ****    ' ' * 1 + '*' * 4
  ***
   **
    *    ' ' * 4 + '*' * 1
'''
for i in range(5):
    print(' ' * i + '*' * (5-i))
